import sortedCharacters from '../game';

test('Sort list of characters', () => {
  const listToSort = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const result = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  expect(sortedCharacters(listToSort)).toEqual(result);
});
